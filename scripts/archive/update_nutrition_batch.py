def update_nutrition_data(db_path):
    """更新营养成分数据"""
    conn = sqlite3.connect(db_path)
    cursor = conn.cursor()
    
    # 准备SQL语句
    sql = """
    UPDATE custom_foods 
    SET calcium_mg = ?,
        trans_fat_g = ?,
        saturated_fat_g = ?,
        sugar_g = ?,
        vitamin_a_ug = ?,
        vitamin_c_mg = ?,
        iron_mg = ?,
        zinc_mg = ?,
        updated_at = CURRENT_TIMESTAMP
    WHERE name = ?
    """
    
    updated_count = 0
    not_found = []
    
    for name, values in BATCH_DATA.items():
        # 检查食材是否存在
        cursor.execute("SELECT id FROM custom_foods WHERE name = ?", (name,))
        result = cursor.fetchone()
        
        if result:
            cursor.execute(sql, (*values, name))
            updated_count += cursor.rowcount
        else:
            not_found.append(name)
    
    conn.commit()
    conn.close()
    
    return updated_count, not_found

